        Fz_front_axle1    = mean(s.tyre.tractorFz(idx,1))+ mean(s.tyre.tractorFz(idx,2));
        Fz_front_axle2     = mean(s.tyre.tractorFz(idx,9))+ mean(s.tyre.tractorFz(idx,10))+mean(s.tyre.tractorFz(idx,11))+ mean(s.tyre.tractorFz(idx,12));
        Fz_rear_axle1     = mean(s.tyre.tractorFz(idx,5))+ mean(s.tyre.tractorFz(idx,6))+mean(s.tyre.tractorFz(idx,7))+ mean(s.tyre.tractorFz(idx,8));
        Fz_rear_axle2    = mean(s.tyre.tractorFz(idx,3))+ mean(s.tyre.tractorFz(idx,4));
    end
    mass_haulingunit=Fz_front_axle1+Fz_front_axle2+Fz_rear_axle1+Fz_rear_axle2;
