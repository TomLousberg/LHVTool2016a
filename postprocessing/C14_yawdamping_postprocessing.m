temps2=temps(mesure2);
yawrate2=yawrate(mesure2);

[maxtab, mintab] = peakdet(yawrate2, 0.001, temps2);

if (numel(maxtab)~=0) && (numel(mintab)~=0)
    tab=[maxtab; mintab];
    tab1=abs([maxtab(:,2); mintab(:,2)]);
    maxima=sort(tab1,'descend');
    maxima=abs(maxima);
    n=length(maxima);

    if n>=6
        A_bar=0;
        for k=1:(n-2)
            A_bar=A_bar+abs(maxima(k)/maxima(k+2));
        end
        A_bar=A_bar/(n-2);
        dampingratio=(log(A_bar))/(sqrt((2*pi)*(2*pi)+(log(A_bar))*(log(A_bar))));

    elseif n<6
        A_bar=0;
        for k=1:(n-1)
            A_bar=A_bar+abs(maxima(k)/maxima(k+1));
        end
        A_bar=A_bar/(n-1);
        dampingratio=(log(A_bar))/(sqrt((pi)*(pi)+(log(A_bar))*(log(A_bar))));
    end

    % Yaw Rate
    S.axes3 = axes('Parent',S.PBS_tab,'units','normalized','position',[.05 0.05 .9 .4]);
    plot(S.axes3, temps,yawrate,'r-.','LineWidth',1.5);
    hold on
    plot(S.axes3, temps1,yawrate1,'k-','LineWidth',2)
    hold on
    plot(S.axes3, tab(:,1), tab(:,2), 'g*');
    title('Yaw Rate');
    legend('Yaw Rate','5%','Extrema');
    xlabel('t [s]');
    ylabel('[deg/s]');
    grid on;

    %Level Performance
    dampingratio=(round(dampingratio*100))/100;
    if dampingratio>1000000
        set(S.level_edit, 'string', 'No');
        Level = 'No';
    elseif dampingratio>=0.15
        set(S.level_edit, 'string', 'Yes');
        Level = 'Yes';
    else
        set(S.level_edit, 'string', 'No');
        Level = 'No';
    end
    set(S.dampingratio_edit, 'string', num2str(dampingratio));   

elseif (numel(maxtab)==0) || (numel(mintab)==0)
    set(S.level_edit, 'string', 'Yes');
    Level = 'Yes';
    set(S.dampingratio_edit, 'string', 'inf');    
    
    % Yaw Rate
    S.axes3 = axes('Parent',S.PBS_tab,'units','normalized','position',[.05 0.05 .9 .4]);
    plot(S.axes3, temps,yawrate,'r-.','LineWidth',1.5);
    title('Yaw Rate');
    xlabel('t [s]');
    ylabel('[deg/s]');
    grid on;
end    

% Safe data in Excel file
if Excelsave == 1
    A = {dampingratio,Level};
    sheet = 1;
