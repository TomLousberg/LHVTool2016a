function run_yawdamping_visualization_callback(eventdata, source)
global Truckname S yawdamping_results yawdamping_filename results  
global yawdamping_wrl yawdamping_sim

% Press the toggle button
set(S.yawdamping_stop_vis_push, 'Value',0);

% Update the VR Sink block
if results == 1
    vrmodelname  = ['simresults/C14_YawDamping/LZV_custom/',yawdamping_results,'/',yawdamping_sim];
    load_system(vrmodelname)    
    [~,name,~] = fileparts(yawdamping_sim);
    h_vrsink = [name,'/VR sink'];
else
    vrmodelname = ['simresults/C14_YawDamping/LZV_custom/',Truckname,'/',Truckname,'_anim_yawdamping.slx'];  
    load_system(vrmodelname)
    h_vrsink=find_system('referenceblock','vrlib/VR Sink');
    h_vrsink=char(h_vrsink(1));
end
x = get_param(h_vrsink, 'FieldsWritten');
if results == 1
    set_param(h_vrsink, 'WorldFileName', yawdamping_wrl);
else
    set_param(h_vrsink, 'WorldFileName', [Truckname,'_yawdamping.wrl']);
end
set_param(h_vrsink, 'FieldsWritten', x);
save_system(vrmodelname)

if isempty(yawdamping_results) == 0
    dirName = ['simresults\C14_YawDamping\LZV_custom\',yawdamping_results];
    files = dir( fullfile(dirName,'*.mat') );
    filename = files.name;
    load(filename,'VR','s','dt');
else
    load(['simresults/C14_YawDamping/LZV_custom/',Truckname,'/',yawdamping_filename])

end       
assignin('base','VR',VR);
assignin('base','s',s);
assignin('base','dt',dt);

% Start simulation
sim(vrmodelname)

% Set the toggle buttons to the right value when simulation is done
set(S.yawdamping_stop_vis_push, 'Value',1);
set(S.yawdamping_run_vis_push, 'Value',0);

